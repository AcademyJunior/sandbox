package app.hero;

public class Hero {

    private String name;
    private int strength;
    private int agility;
    private int intelligence;

    public String getName() {
        return name;
    }

    public int getStrength() {
        return strength;
    }

    public int getAgility() {
        return agility;
    }

    public int getIntelligence() {
        return intelligence;
    }
}
